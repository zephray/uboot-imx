static void early_init_pmic(void)
{
	int ret;
#ifdef CONFIG_DM_I2C
	struct udevice *bus, *dev;
#endif

#ifdef CONFIG_DM_I2C
	ret = uclass_get_device_by_seq(UCLASS_I2C, 0, &bus);
#else
	ret = i2c_set_bus_num(0);
#endif
	if (ret) {
		printf("Failed to get I2C bus: %d\n", ret);
		return;
	}

#ifdef CONFIG_DM_I2C
	ret = dm_i2c_probe(bus, 0x34, DM_I2C_CHIP_RD_ADDRESS |
			DM_I2C_CHIP_WR_ADDRESS, &dev);
#else
	ret = i2c_probe(0x34);
#endif
	if (ret) {
		printf("PMIC didn't respond! %d\n", ret);
		return;
	}

#ifdef CONFIG_DM_I2C
	#define I2C_WRITE(addr) dm_i2c_write(dev, addr, &buf, 1)
	#define I2C_READ(addr) dm_i2c_read(dev, addr, &buf, 1)
#else
	#define I2C_WRITE(addr) i2c_write(0x34, addr, 1, &buf, 1)
	#define I2C_READ(addr) i2c_read(0x34, addr, 1, &buf, 1)
#endif

	uint8_t buf;
	// Set DCDC2 to 1.375V
	buf = 27;
	I2C_WRITE(0x23);
	// Set DCDC3 to 1.35V
	buf = 26;
	I2C_WRITE(0x27);
	// Set LDO3 to 3.3V
	buf = 104;
	I2C_WRITE(0x29);
	// Read back DCDC2 voltage
	I2C_READ(0x23);
	printf("VCORE set to %d mV\n", buf * 25 + 700);
	// Read back DCDC3 voltage
	I2C_READ(0x27);
	printf("VDDR set to %d mV\n", buf * 25 + 700);
	// Read back LDO3 voltage
	I2C_READ(0x29);
	printf("VSD set to %d mV\n", buf * 25 + 700);
}

void board_init_f(ulong dummy)
{
	ccgr_init();

	/* setup AIPS and disable watchdog */
	arch_cpu_init();

	/* iomux and setup of i2c */
	board_early_init_f();

	/* setup GP timer */
	timer_init();

	/* UART clocks enabled and gd valid - init serial console */
	preloader_console_init();

	early_init_pmic();

	/* DDR initialization */
	spl_dram_init();

	/* Clear the BSS. */
	memset(__bss_start, 0, __bss_end - __bss_start);

	/* load/boot image from boot device */
	board_init_r(NULL, 0);
}
